int	ft_strcmp(char *s1, char *s2)
{
	while (*s1)
	{
		if (!*s2)
			return (*s1 - *s2);
		else if (*s1 > *s2)
			return (*s1 - *s2);
		else if (*s1 < *s2)
			return (-(*s2 - *s1));
		s1++;
		s2++;
	}
	if (*s1 < *s2)
		return (-(*s2 - *s1));
	return (0);
}
